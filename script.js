let currentStep = 1;
const totalSteps = 4;
let userAnswers = {};

function startQuiz() {
    // Сбрасываем ответы и текущий шаг
    userAnswers = {};
    currentStep = 1;
    
    // Скрываем все секции
    document.querySelectorAll('main > section').forEach(section => {
        section.classList.add('hidden');
    });
    
    // Показываем квиз
    document.getElementById('quiz').classList.remove('hidden');
    
    // Сбрасываем выбранные опции
    document.querySelectorAll('.option-card').forEach(card => {
        card.classList.remove('selected');
    });
    
    // Сбрасываем прогресс-бар
    document.querySelector('.progress').style.width = '25%';
    
    // Показываем первый шаг
    showStep(1);
    
    // Сбрасываем кнопки навигации
    document.getElementById('prevBtn').classList.add('hidden');
    document.getElementById('nextBtn').textContent = 'Далее';
    document.getElementById('nextBtn').disabled = true;
}

function showStep(step) {
    // Скрываем все шаги
    document.querySelectorAll('.quiz-step').forEach(el => {
        el.classList.add('hidden');
        el.classList.remove('active');
    });
    
    // Показыва��м текущий шаг
    const currentStepEl = document.querySelector(`.quiz-step[data-step="${step}"]`);
    currentStepEl.classList.remove('hidden');
    setTimeout(() => currentStepEl.classList.add('active'), 10);
    
    updateProgress();
    updateNavigationButtons();
}

function updateProgress() {
    const progress = (currentStep / totalSteps) * 100;
    document.querySelector('.progress').style.width = `${progress}%`;
}

function updateNavigationButtons() {
    const prevBtn = document.getElementById('prevBtn');
    const nextBtn = document.getElementById('nextBtn');
    
    prevBtn.classList.toggle('hidden', currentStep === 1);
    nextBtn.textContent = currentStep === totalSteps ? 'Завершить' : 'Далее';
}

function selectOption(element) {
    // Убираем выделение у всех опций текущего шага
    const stepElement = element.closest('.quiz-step');
    stepElement.querySelectorAll('.option-card').forEach(el => {
        el.classList.remove('selected');
    });
    
    // Выделяем выбранную опцию
    element.classList.add('selected');
    
    // Сохраняем значение из data-value
    userAnswers[currentStep] = element.getAttribute('data-value');
    
    // Актируем кнопку "Далее"
    document.getElementById('nextBtn').disabled = false;
}

function previousStep() {
    if (currentStep > 1) {
        currentStep--;
        showStep(currentStep);
    }
}

function nextStep() {
    const currentStepEl = document.querySelector(`.quiz-step[data-step="${currentStep}"]`);
    const selectedOption = currentStepEl.querySelector('.option-card.selected');
    
    if (!selectedOption) {
        alert('Пожалуйста, выберите один из вариантов');
        return;
    }
    
    if (currentStep < totalSteps) {
        currentStep++;
        showStep(currentStep);
    } else {
        showResults();
    }
}

function showResults() {
    console.log('Показываем результаты');
    
    const resultsElement = document.getElementById('results');
    const quizElement = document.getElementById('quiz');
    
    if (!resultsElement || !quizElement) {
        console.error('Не найдены элементы DOM');
        return;
    }
    
    // Скрываем квиз и показываем результаты
    quizElement.classList.add('hidden');
    resultsElement.classList.remove('hidden');
    
    try {
        // Получаем программу
        const program = selectProgram(userAnswers);
        
        if (!program) {
            throw new Error('Программа не найдена');
        }
        
        // Формируем HTML для отображения программы
        resultsElement.innerHTML = `
            <div class="program-container">
                <h2>${program.name}</h2>
                <div class="program-content">
                    ${program.description.split('\n').map(line => 
                        line.trim() ? `<p>${line}</p>` : '<br>'
                    ).join('')}
                </div>
                <button onclick="startQuiz()" class="restart-btn">Начать заново</button>
            </div>
        `;
        
    } catch (error) {
        console.error('Ошибка при отображении результатов:', error);
        resultsElement.innerHTML = `
            <div class="error-container">
                <h2>Произошла ошибка</h2>
                <p>К сожалению, не удалось сгенерировать программу тренировок. 
                   Пожалуйста, попробуйте еще раз.</p>
                <button onclick="startQuiz()" class="restart-btn">Начать заново</button>
            </div>
        `;
    }
}

