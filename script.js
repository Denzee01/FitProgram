function generateProgram() {
    console.log('Генерируем программу');
    const program = selectProgram(userAnswers);
    const resultsContainer = document.getElementById('results');
    
    if (!program) {
        console.error('Программа не опрделена');
        resultsContainer.innerHTML = '<p>роизошла ошибка при генерации рораммы</p>';
        return;
    }
    
    // Создаем HTML для программы тренировок
    let programHTML = `
        <h2 class="program-title">${program.name}</h2>
        
        <div class="recommendations">
            <h3>Рекомендации:</h3>
            <ul>
                ${Object.entries(program.recommendations || {}).map(([key, value]) => 
                    `<li><strong>${key}:</strong> ${value}</li>`
                ).join('')}
            </ul>
        </div>
        
        <div class="schedule">
            <h3>Расписание тренировок:</h3>
            ${program.schedule.map(day => `
                <div class="training-day">
                    <h4>${day.day}</h4>
                    <ul class="exercises-list">
                        ${day.exercises.map(ex => `
                            <li class="exercise-item">
                                <span class="exercise-name">${ex.name}</span>
                                ${ex.sets ? `<span class="exercise-sets">${ex.sets} подходов</span>` : ''}
                                ${ex.reps ? `<span class="exercise-reps">по ${ex.reps}</span>` : ''}
                                ${ex.duration ? `<span class="exercise-duration">${ex.duration}</span>` : ''}
                            </li>
                        `).join('')}
                    </ul>
                </div>
            `).join('')}
        </div>
        
        <button onclick="downloadProgram()" class="download-btn">Скачать программу</button>
    `;
    
    resultsContainer.innerHTML = programHTML;
}

// Добавим новую функцию дл скачивания программы
function downloadProgram() {
    const program = selectProgram(userAnswers);
    
    let content = `
        ${program.name}
        
        Рекомендации:
        ${Object.entries(program.recommendations || {}).map(([key, value]) => 
            `${key}: ${value}`
        ).join('\n')}
        
        Расписание:
        ${program.schedule.map(day => `
            ${day.day}
            ${day.exercises.map(ex => 
                `- ${ex.name}: ${ex.sets ? `${ex.sets} подходов по ` : ''}${ex.reps || ex.duration}`
            ).join('\n')}
        `).join('\n')}
    `;
    
    const blob = new Blob([content], { type: 'text/plain' });
    const url = window.URL.createObjectURL(blob);
    const a = document.createElement('a');
    a.href = url;
    a.download = 'training-program.txt';
    a.click();
    window.URL.revokeObjectURL(url);
}

// Заменим функцию selectProgram на новую версию
function selectProgram(userAnswers) {
    console.log('Выбор программы на основе ответов:', userAnswers);
    
    const goal = userAnswers[1];         // Цель (weightLoss/muscleGain)
    const level = userAnswers[2];        // Уровень (beginner/advanced)
    const restriction = userAnswers[3];   // Ограничения (none/back/joints/cardio)
    
    // Проверяем наличие всех необходимых ответов
    if (!goal || !level || !restriction) {
        console.error('Не все ответы получены:', { goal, level, restriction });
        return null;
    }

    try {
        // Сначала проверяем продвинутый уровень
        if (level === 'advanced') {
            if (goal === 'weightLoss') {
                switch (restriction) {
                    case 'none': return trainingPrograms.advanced.weightLoss;
                    case 'back': return trainingPrograms.advanced.weightLossBack;
                    case 'joints': return trainingPrograms.advanced.weightLossJoints;
                    default: return trainingPrograms.advanced.weightLoss;
                }
            } else if (goal === 'muscleGain') {
                switch (restriction) {
                    case 'back': return trainingPrograms.advanced.weightGainBackAdvanced;
                    default: return trainingPrograms.advanced.weightGainAdvanced;
                }
            }
        }
        // Затем проверяем начальный уровень
        else if (level === 'beginner') {
            if (goal === 'weightLoss') {
                switch (restriction) {
                    case 'none': return trainingPrograms.beginner.weightLoss;
                    case 'back': return trainingPrograms.beginner.weightLossBack;
                    case 'joints': return trainingPrograms.beginner.weightLossJoints;
                    case 'cardio': return trainingPrograms.beginner.weightLossCardio;
                    default: return trainingPrograms.beginner.weightLoss;
                }
            } else if (goal === 'muscleGain') {
                switch (restriction) {
                    case 'back': return trainingPrograms.beginner.weightGainBackBeginner;
                    case 'joints': return trainingPrograms.beginner.weightGainJointsBeginner;
                    default: return trainingPrograms.beginner.weightGainBeginner;
                }
            }
        }

        // Если не нашли подходящую программу, возвращаем базовую
        console.log('Возвращаем базовую программу');
        return trainingPrograms.beginner.weightLoss;

    } catch (error) {
        console.error('Ошибка при выборе программы:', error);
        return null;
    }
}

function validateForm() {
    const questions = document.querySelectorAll('.question');
    for (let question of questions) {
        const answers = question.querySelectorAll('input[type="radio"]:checked');
        if (answers.length === 0) {
            alert('Пожалуйста, ответьте на все вопросы');
            return false;
        }
    }
    return true;
} 